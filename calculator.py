a = 10
b = 24
print("The value of", a, "+", b, "is:", a+b)
print("The value of", a, "-", b, "is:", a-b)
print("The value of", a, "/", b, "is:", a/b)
print("The value of", a, "*", b, "is:", a*b)
print("The value of", a, "**", b, "is:", a**b)
# modulus
print("The value of", a, "%", b, "is:", a % b)
# exponential
print("The value of", a, "**", b, "is:", a**b)
# floor division
print("The value of", a, "//", b, "is:", a//b)
